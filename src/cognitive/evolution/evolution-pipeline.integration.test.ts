// SCENARIO 6: Lifecycle — usage tracking, staleness, expiry
// ===========================================================================
describe("Pipeline: skill lifecycle management", () => {
  it("tracks usage via touchSkill", async () => {
    await writer.writeSkill({
      name: "lifecycle-test",
      description: "Test lifecycle",
      triggerPhrases: ["test"],
      bodyMarkdown: "Body",
    });

    const meta0 = await writer.readSkillMeta("lifecycle-test");
    expect(meta0).not.toBeNull();
    expect(meta0!.usageCount).toBe(0);

    await writer.touchSkill("lifecycle-test");
    const meta1 = await writer.readSkillMeta("lifecycle-test");
    expect(meta1!.usageCount).toBe(1);

    await writer.touchSkill("lifecycle-test");
    const meta2 = await writer.readSkillMeta("lifecycle-test");
    expect(meta2!.usageCount).toBe(2);
    expect(meta2!.lastUsedAt).toBeGreaterThan(0);
  });

  it("detects stale skills", async () => {
    const staleCandidate: EvolutionCandidate = {
      taskSummary: "old task",
      toolCalls: ["tool"],
      uniqueToolCount: 1,
      reasoningTurns: 1,
      durationMs: 1000,
      domain: "old",
    };
    const draft = generateSkillDraft(staleCandidate);

    // Write with normal creation
    await writer.writeSkill(draft);

    // Force lastUsedAt to 31 days ago
    const raw = await writer.readRawSkill(draft.name);
    expect(raw).not.toBeNull();
    const staleTimestamp = Date.now() - 31 * 86400000;
    const patched = raw!.replace(
      /lastUsedAt:\s*\d+/,
      `lastUsedAt: ${staleTimestamp}`,
    );
    await writer.updateSkill(draft.name, patched);

    const meta = await writer.readSkillMeta(draft.name);
    expect(meta).not.toBeNull();
    expect(meta!.isStale).toBe(true);
  });

  it("removes stale skills with zero usage", async () => {
    // Create two skills: one stale+unused, one recent
    await writer.writeSkill({
      name: "stale-skill",
      description: "Old skill",
      triggerPhrases: ["old"],
      bodyMarkdown: "Old",
    });
    await writer.writeSkill({
      name: "fresh-skill",
      description: "Fresh skill",
      triggerPhrases: ["new"],
      bodyMarkdown: "Fresh",
    });

    // Force stale-skill to be old and unused
    const raw = await writer.readRawSkill("stale-skill");
    const staleTs = Date.now() - 31 * 86400000;
    const patched = raw!.replace(/lastUsedAt:\s*\d+/, `lastUsedAt: ${staleTs}`).replace(/usageCount:\s*\d+/, "usageCount: 0");
    await writer.updateSkill("stale-skill", patched);

    const removed = await lifecycle.removeStale(30);
    expect(removed).toBe(1);

    expect(await writer.skillExists("stale-skill")).toBe(false);
    expect(await writer.skillExists("fresh-skill")).toBe(true);
  });

  it("does NOT remove stale skills that have been used", async () => {
    await writer.writeSkill({
      name: "used-but-stale",
      description: "Used but old",
      triggerPhrases: [],
      bodyMarkdown: "Body",
    });

    // Make stale but with usageCount > 0
    const raw = await writer.readRawSkill("used-but-stale");
    const staleTs = Date.now() - 31 * 86400000;
    const patched = raw!
      .replace(/lastUsedAt:\s*\d+/, `lastUsedAt: ${staleTs}`)
      .replace(/usageCount:\s*\d+/, "usageCount: 5");
    await writer.updateSkill("used-but-stale", patched);

    const removed = await lifecycle.removeStale(30);
    expect(removed).toBe(0);
    expect(await writer.skillExists("used-but-stale")).toBe(true);
  });
});

