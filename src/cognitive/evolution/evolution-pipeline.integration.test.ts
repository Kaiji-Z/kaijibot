function complexCandidate(overrides: Partial<EvolutionCandidate> = {}): EvolutionCandidate {
  return {
    taskSummary: "归档产品评审会议纪要到知识库并创建飞书任务",
    toolCalls: [
      "feishu_vc_search",
      "feishu_vc_notes",
      "feishu_doc_fetch",
      "feishu_wiki_spaces",
      "feishu_wiki_create",
      "feishu_doc_write",
      "feishu_task_create",
    ],
    uniqueToolCount: 6,
    reasoningTurns: 8,
    durationMs: 180_000,
    domain: "feishu-meeting",
    ...overrides,
  };
}

function simpleCandidate(): EvolutionCandidate {
  return {
    taskSummary: "查询天气",
    toolCalls: ["weather_get"],
    uniqueToolCount: 1,
    reasoningTurns: 1,
    durationMs: 2_000,
    domain: "weather",
  };
}

function trialErrorCandidate(): EvolutionCandidate {
  return complexCandidate({
    transcript: [
      "用户: 不对，不是这个文档",
      "Agent: 抱歉，我来重新查找",
      "用户: 换一种方式试试",
      "tool_call:feishu_doc_search",
      "tool_call:feishu_doc_search",
      "tool_call:feishu_doc_search",
      "Agent: 搞错了，让我重新来",
    ].join("\n"),
    hasTrialAndError: true,
    userCorrections: 2,
  });
}

function wellFormedSkillMd(name: string, description: string): string {
  return [
    "---",
    `name: ${name}`,
    `description: "${description}"`,
    "---",
    "",
    "## Triggers",
    "",
    `- ${name} 操作`,
    `- ${name} task`,
    `- 帮我${description}`,
    "",
    "## Workflow",
    "",
    "1. Do the first step.",
    "2. Do the second step.",
    "",
    "## Notes",
    "",
    "Auto-generated by self-evolution engine.",
  ].join("\n");
}

